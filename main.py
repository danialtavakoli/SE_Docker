def multiply(a, b):
    return a * b

def add(a, b):
    return a + b

def divide(a, b):
    return a / b

def subtract(a, b):
    return a - b
  
result = multiply(2, 3)
print(result)  # prints 6

result = add(2, 3)
print(result)  # prints 5

result = divide(10, 2)
print(result)  # prints 5.0

result = subtract(10, 2)
print(result)  # prints 8
